
public class funcionario {

    public int codigoFuncional;
    public String nome;
    public String cargo;
    public double salario;

    public funcionario(int codigoFuncional, String nome, String cargo) {
        this.codigoFuncional = codigoFuncional;
        this.nome = nome;
        this.cargo = cargo;
        this.salario = salario;

    }

    public int getcodigoFuncional() {
        return this.codigoFuncional;
    }

    public int setcodigoFuncional() {
        return this.codigoFuncional;
    }

    public String getnome() {
        return this.nome;
    }

    public String setnome() {
        return this.nome;
    }

    public String getcargo() {
        return this.cargo;
    }

    public String setcargo() {
        return this.cargo;
    }

    public double getsalario() {
        return this.salario;
    }

    public double setsalario() {
        return this.salario;
    }
}
